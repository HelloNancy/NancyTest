public class var1{
    public static void main(String[] args) {
        //声明一个变量
        int a = 10;
        int b = 20;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b); //如果运算两端都是int，结果就是int，取的整数
        System.out.println(a%b);//取的余数
    }
}
